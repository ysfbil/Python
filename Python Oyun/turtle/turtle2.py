import turtle

t=turtle.Turtle()

t.speed(10)
t.clear()
t.circle(5,3,2)
t.circle(10)
t.circle(100)
t.goto(20,50)
t.write("selam")
t.goto(200,200)
t.begin_fill()
t.circle(30)
t.end_fill()
t.color("red")
t.begin_fill()
t.forward(100)
t.right(95)
t.forward(200)
t.left(90)
t.left(180)
t.forward(100)

t.right(90)
t.forward(200)
t.end_fill()
t.goto(39,0)
t.color("black")
t.pencolor("black")
t.penup()
t.backward(500)
t.forward(300)
t.left(90)
t.forward(200)
t.pendown()
t.forward(100)
t.showturtle()
t.up()

t.write(t.ycor())
